// Package answer — нормализация и сравнение ответов математического режима.
package answer

import "strings"

// Normalize приводит ответ к каноническому виду для сравнения:
// убирает крайние пробелы, схлопывает любые последовательности пробельных
// символов (пробелы, табы, переводы строк, юникод-пробелы) в один пробел
// и приводит к нижнему регистру. Сравнение — на точное совпадение
// нормализованных строк.
func Normalize(s string) string {
	// strings.Fields разбивает по любым пробельным символам и отбрасывает
	// пустые фрагменты — этого достаточно для нормализации пробелов.
	return strings.ToLower(strings.Join(strings.Fields(s), " "))
}

// Equal сообщает, совпадают ли ответы после нормализации.
func Equal(a, b string) bool {
	return Normalize(a) == Normalize(b)
}
